def areaofcircle():
    radius=float(input('ENTER THE RADIUS:-'))
    print("THE AREA OF CIRCLE IS:",(3.14*radius*radius))

def areofrectangle():
    length=int(input('ENTER THE LENGTH OF RECTANGLE:-'))
    breath=int(input('ENTER THE BREATH OF REACTANGLE:-'))
    print('AREA OF REACTANGLE IS:',(length*breath))

areaofcircle()
areofrectangle()
